/* ─── globals ───────────────────────────────────────────────── */
let provider, signer, swapContract;

/* ─── helper: pick MetaMask provider even if Phantom exists ── */
function getMetaMaskProvider() {
  if (window.ethereum?.providers?.length) {
    return window.ethereum.providers.find(p => p.isMetaMask) || null;
  }
  return window.ethereum?.isMetaMask ? window.ethereum : null;
}

/* ─── UI shortcut ── */
const $ = id => document.getElementById(id);

/* ─── connect / disconnect logic ────────────────────────────── */
async function connectWallet() {
  const metamask = getMetaMaskProvider();
  if (!metamask) {
    alert("MetaMask not detected. Disable Phantom or install MetaMask.");
    return;
  }

  try {
    await metamask.request({ method: "eth_requestAccounts" });

    provider = new ethers.providers.Web3Provider(metamask, "any");
    signer   = provider.getSigner();
    const addr = await signer.getAddress();

    swapContract = new ethers.Contract(SIMPLE_SWAP, abi, signer);

    localStorage.setItem("connectedWallet", addr);
    updateConnectionStatus(addr);
  } catch (err) {
    console.error("connectWallet error:", err);
    alert(`Connection failed: ${err.message || err}`);
  }
}

function disconnectWallet() {
  localStorage.removeItem("connectedWallet");
  provider = signer = swapContract = null;
  updateConnectionStatus(null);
}

function updateConnectionStatus(addr) {
  const btn = $("connectBtn");
  const label = $("accountAddress");

  if (addr) {
    label.textContent = `Connected to: ${addr}`;
    btn.textContent   = "Disconnect";
    btn.onclick       = disconnectWallet;
  } else {
    label.textContent = "Not connected";
    btn.textContent   = "Connect";
    btn.onclick       = connectWallet;
  }
}

/* metaMask account switch */
if (window.ethereum) {
  window.ethereum.on("accountsChanged", accounts => {
    if (accounts.length === 0) disconnectWallet();
    else updateConnectionStatus(accounts[0]);
  });
}

/* ─── approve-if-needed + swap ──────────────────────────────── */
async function performSwap() {
  if (!swapContract) {
    alert("Connect your wallet first.");
    return;
  }

  try {
    const amountInStr = $("amountA").value;
    if (!amountInStr || isNaN(Number(amountInStr))) {
      alert("Enter a valid Token A amount.");
      return;
    }

    const amountIn  = ethers.utils.parseUnits(amountInStr, DECIMALS);
    const userAddr  = await signer.getAddress();
    const tokenAERC20 = new ethers.Contract(
      TOKEN_A,
      [
        "function approve(address,uint256) public returns(bool)",
        "function allowance(address,address) public view returns(uint256)"
      ],
      signer
    );

    const allowance = await tokenAERC20.allowance(userAddr, SIMPLE_SWAP);
    if (allowance.lt(amountIn)) {
      const txA = await tokenAERC20.approve(SIMPLE_SWAP, amountIn);
      await txA.wait();
      console.log("Allowance updated");
    }

    const deadline = Math.floor(Date.now() / 1000) + 600;
    const tx = await swapContract.swapExactTokensForTokens(
      amountIn,
      0,                  // amountOutMin
      [TOKEN_A, TOKEN_B], // path
      userAddr,
      deadline
    );

    await tx.wait();
    alert("Swap completed!");
  } catch (err) {
    console.error("performSwap error:", err);
    alert(`Swap failed: ${err.message || err}`);
  }
}

/* ─── boot ──────────────────────────────────────────────────── */
window.addEventListener("load", () => {
  const stored = localStorage.getItem("connectedWallet");
  updateConnectionStatus(stored);
});

/* expose to HTML */
window.connectWallet = connectWallet;
window.performSwap   = performSwap;